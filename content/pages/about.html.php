<h2>About Cosplay Academy</h2>
CosPlay Academy is a prequel to the weekly 
<a href="http://www.crawlcosplay.com/" target="_blank" title="Visit the CCC website">Crawl Cosplay Challenge</a> (CCC).
<p>The intended audience is DCSS players who are fairly new to the game and don't feel they have the skills necessary yet 
to compete in the Crawl Cosplay Challenge at a level that isn't just beginner.

