<h2>About Cosplay Academy</h2>
Crawl's CosPlay Academy (CCA) is a prequel to the weekly 
<a href="http://www.crawlcosplay.com/" target="_blank" title="Visit the CCC website">Crawl Cosplay Challenge</a> (CCC).
<p>The intended audience is <abbr title="Dungeon Craw: Stone Soup">DCSS</abbr> players who are fairly new to the game or haven't won at least 4 games with each of the different styled combos.
It's also for players who don't feel they have the skills necessary yet to compete in the Crawl Cosplay Challenge at a level that isn't just beginner.
