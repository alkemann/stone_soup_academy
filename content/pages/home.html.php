<h2>Welcome "Weakling"</h2>
<p>Do you have dreams of becoming a "Journeyman" or even an "Adept" crawler? You'll first need to win a game to get the title of "Amateur"
and then a 2nd win to join the ranks of "Novice". But fear not, your dream is the goal of the Cosplay Academy to:
<li>provide you with a structured approach to ease the learning curve while playing characters based on Uniques;</li> 
in order to
    <li>better compete in the <a href="http://www.crawlcosplay.com/">weekly Crawl Cosplay Challenge.</li>

<p>Your first Academy goal is to play a melee brute since they are considered to be "easier". 
    (Other character sets will be developed at a later date.) Also, the assigned Gods are considered part of the better ones.</p>
<p>Once you have finished a game, <a href="/submit">Submit your Morque</a> for <a href="/scoring">Scoring</a>. 
    This will allow you to be added to the <a href="/scoreboard">Scoreboard</a>(as an 
    <i><a href="https://crawl.kelbi.org/morgue-test/best-players-total-score.html">example</a></i>) so that you can track your progress 
    and also compare yours to others'.</p>
<p>If you are fairly new to DCSS, the following character guides should be very helpful.</p>
